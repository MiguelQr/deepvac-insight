def apply_translations(ts_path, translations, plurals):
    tree = ET.parse(ts_path)
    root = tree.getroot()
    missing = set()
    filled = 0
    for ctx in root.findall("context"):
        for msg in ctx.findall("message"):
            source_el = msg.find("source")
            src = source_el.text
            translation_el = msg.find("translation")
            if msg.get("numerus") == "yes":
                forms = plurals.get(src)
                if forms is None:
                    missing.add(src)
                    continue
                for numerus_el, text in zip(translation_el.findall("numerusform"), forms):
                    numerus_el.text = text
                if "type" in translation_el.attrib:
                    del translation_el.attrib["type"]
                filled += 1
            else:
                if src not in translations:
                    missing.add(src)
                    continue
                translation_el.text = translations[src]
                if "type" in translation_el.attrib:
                    del translation_el.attrib["type"]
                filled += 1
    tree.write(ts_path, encoding="utf-8", xml_declaration=True)
    return filled, missing


es_filled, es_missing = apply_translations(HERE / "deepvac_es.ts", ES, PLURALS_ES)
